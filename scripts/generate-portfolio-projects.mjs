import fs from "node:fs";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");

function slugToExportName(slug) {
  const parts = slug.split("-");
  const name = parts
    .map((part) => (/^\d/.test(part) ? `n${part}` : part.charAt(0).toUpperCase() + part.slice(1)))
    .join("");
  return name.charAt(0).toLowerCase() + name.slice(1);
}

function slugToComponentName(slug) {
  const exportName = slugToExportName(slug);
  return exportName.charAt(0).toUpperCase() + exportName.slice(1) + "PageContent";
}

function isPlaceholderImage(url) {
  return url.startsWith("https://images.unsplash.com/");
}

function serializeString(value) {
  return JSON.stringify(value);
}

function serializeProject(exportName, project, todoComments = []) {
  const lines = [`import type { Project } from "./types";`, ""];

  for (const comment of todoComments) {
    lines.push(`// TODO: ${comment}`);
  }

  if (isPlaceholderImage(project.heroImage)) {
    lines.push("// TODO: Replace placeholder heroImage with project-specific photography");
  }

  if (project.gallery.some((image) => isPlaceholderImage(image))) {
    lines.push("// TODO: Replace placeholder gallery images with project-specific photography");
  }

  lines.push(`export const ${exportName}: Project = ${JSON.stringify(project, null, 2)};`);
  lines.push("");
  return lines.join("\n");
}

function buildHighlights(project) {
  return project.services.slice(0, 3).map((title) => ({
    title,
    description: `MisrConsult delivered ${title.toLowerCase()} as part of the engineering scope for ${project.title} in ${project.location}.`,
  }));
}

function getTodoComments(input) {
  const comments = [];
  const raw = JSON.stringify(input);

  if (input.slug === "emaar-marassi-red-sea") {
    comments.push(
      "PDF lists both 5,711 m² (design package) and 2,023,430 m² (buildings) — verify correct primary area",
    );
  }
  if (input.slug === "fifth-settlement-full-design") {
    comments.push("PDF slide does not state a clear project name — verify official title");
  }
  if (input.slug === "mivida-gardens") {
    comments.push('PDF shows "???" for one area field — using 420,000 m² from portfolio slide');
  }
  if (input.slug === "sarai-2-midanat-masr") {
    comments.push("PDF lists both 50 Fed. and 135 Fed. — verify phase breakdown");
  }
  if (input.slug === "labor-camp") {
    comments.push("Verify whether this is industrial or residential worker housing");
  }
  if (input.slug === "al-khafji-joint-operations-housing") {
    comments.push("PDF shows 1,400,000 KM² — likely a typo for m²");
  }
  if (input.slug === "jubail-industrial-college") {
    comments.push("Verify category — may also fit Educational Facilities");
  }
  if (input.slug === "damam-water-tower") {
    comments.push("PDF slide contains garbled text — verify project details");
  }

  if (!input.year) {
    comments.push("Confirm completion year");
  }
  if (!input.client) {
    comments.push("Confirm client name");
  }

  return comments;
}

async function main() {
  const portfolioDataUrl = pathToFileURL(path.join(root, "lib/projects/portfolio-data.ts")).href;
  const helpersUrl = pathToFileURL(path.join(root, "lib/projects/portfolio-helpers.ts")).href;

  const { PORTFOLIO_INPUTS } = await import(portfolioDataUrl);
  const { buildPortfolioProject } = await import(helpersUrl);

  const skipSlugs = new Set(["ora-silversands"]);
  const projects = [];

  for (const input of PORTFOLIO_INPUTS) {
    if (skipSlugs.has(input.slug)) {
      continue;
    }

    const built = buildPortfolioProject(input);
    built.highlights = buildHighlights(built);

    const exportName = slugToExportName(input.slug);
    const componentName = slugToComponentName(input.slug);
    const todoComments = getTodoComments(input);

    const projectFile = path.join(root, "lib/projects", `${input.slug}.ts`);
    fs.writeFileSync(projectFile, serializeProject(exportName, built, todoComments));

    const pageDir = path.join(root, "components/projects", input.slug);
    fs.mkdirSync(pageDir, { recursive: true });

    const pageContent = `import type { Project } from "@/lib/projects";
import { StandardProjectPageContent } from "../standard-project-page-content";

export function ${componentName}({ project }: { project: Project }) {
  return <StandardProjectPageContent project={project} />;
}
`;
    fs.writeFileSync(path.join(pageDir, "page-content.tsx"), pageContent);

    projects.push({ slug: input.slug, exportName, componentName });
  }

  const indexLines = [
    "// Auto-generated by scripts/generate-portfolio-projects.mjs — do not edit manually",
    "",
  ];
  const registryImports = [];
  const registryEntries = [];

  for (const { slug, exportName, componentName } of projects) {
    indexLines.push(`export { ${exportName} } from "./${slug}";`);
    registryImports.push(
      `import { ${componentName} } from "./${slug}/page-content";`,
    );
    registryEntries.push(`  "${slug}": ${componentName},`);
  }

  indexLines.push("");
  indexLines.push('import type { Project } from "./types";');
  for (const { exportName, slug } of projects) {
    indexLines.push(`import { ${exportName} } from "./${slug}";`);
  }
  indexLines.push("");
  indexLines.push("export const portfolioProjectList: Project[] = [");
  for (const { exportName } of projects) {
    indexLines.push(`  ${exportName},`);
  }
  indexLines.push("];");
  indexLines.push("");

  fs.writeFileSync(path.join(root, "lib/projects/portfolio-index.ts"), indexLines.join("\n"));

  const registryContent = `// Auto-generated by scripts/generate-portfolio-projects.mjs — do not edit manually
import type { ComponentType } from "react";
import type { Project } from "@/lib/projects";
${registryImports.join("\n")}

export const portfolioPageContentBySlug = {
${registryEntries.join("\n")}
} as const satisfies Record<string, ComponentType<{ project: Project }>>;
`;

  fs.writeFileSync(
    path.join(root, "components/projects/portfolio-page-registry.tsx"),
    registryContent,
  );

  console.log(`Generated ${projects.length} dedicated project files and page components.`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
